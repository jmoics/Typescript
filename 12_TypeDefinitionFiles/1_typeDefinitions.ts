/* ¿Por qué escribir un type definition?
 * Los archivos de type definition se usan principalmente como contenedor de TypeScript para cualquier archivo JavaScript o biblioteca de JavaScript que se use en el código TypeScript.
 * Se utilizan para verificar el tipo de tiempo de compilación al acceder al código externo de JavaScript dentro del código TypeScript.
 * Proporcionan verificación de tipo, autocompletado y documentación de miembro para el código JavaScript al que se accede con el código TypeScript.

 * ¿Qué es el archivo de type definition?
 * Los archivos de type definition son los archivos que contienen información de tipo para un archivo JavaScript o una biblioteca JavaScript que se utilizará en el código TypeScript.
 * No tienen ninguna definición
 * Los archivos de type definition tienen la extensión .d.ts
 * Tienen declaración de funciones, clases, módulos, etc., que se define en un archivo JavaScript con el código TypeScript.
 */